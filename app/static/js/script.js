document.addEventListener('DOMContentLoaded', function() {
    // Общие функции
    initMobileMenu();
    initImageGallery();
    
    // Инициализация страниц
    if (document.querySelector('.tours-list')) {
        initToursPage();
    }
    
    if (document.querySelector('.tour-detail')) {
        initTourDetailPage();
    }
    
    if (document.querySelector('.reviews')) {
        initReviewsPage();
    }
    
    if (document.querySelector('#review-form')) {
        initReviewForm();
    }
});

function initMobileMenu() {
    const menuToggle = document.createElement('div');
    menuToggle.className = 'menu-toggle';
    menuToggle.innerHTML = '☰';
    menuToggle.addEventListener('click', function() {
        const nav = document.querySelector('.nav ul');
        nav.classList.toggle('active');
    });
    
    const header = document.querySelector('.header .container');
    if (header) {
        header.prepend(menuToggle);
    }
}

function initImageGallery() {
    const thumbnails = document.querySelectorAll('.thumbnails img');
    if (!thumbnails.length) return;
    
    const mainImage = document.querySelector('.main-image img');
    if (!mainImage) return;
    
    thumbnails.forEach(thumb => {
        thumb.addEventListener('click', function() {
            mainImage.src = this.src.replace('150x100', '800x500');
        });
    });
}

function initToursPage() {
    loadTours();
    
    const applyFiltersBtn = document.getElementById('apply-filters');
    if (applyFiltersBtn) {
        applyFiltersBtn.addEventListener('click', loadTours);
    }
    
    const urlParams = new URLSearchParams(window.location.search);
    const regionParam = urlParams.get('region');
    
    if (regionParam && document.getElementById('region')) {
        document.getElementById('region').value = regionParam;
        loadTours();
    }
}

function loadTours() {
    const region = document.getElementById('region')?.value || 'all';
    const type = document.getElementById('type')?.value || 'all';
    const price = document.getElementById('price')?.value || 'all';
    const toursGrid = document.querySelector('.tours-grid');
    
    if (!toursGrid) return;
    
    const mockTours = getMockTours().filter(tour => {
        let match = true;
        
        if (region !== 'all' && tour.region !== region) match = false;
        if (type !== 'all' && tour.type !== type) match = false;
        if (price !== 'all') {
            const [min, max] = price.split('-').map(p => {
                if (p.endsWith('+')) return parseInt(p);
                return parseInt(p) || 0;
            });
            
            if (max && (tour.price < min || tour.price > max)) match = false;
            if (price.endsWith('+') && tour.price < min) match = false;
        }
        
        return match;
    });
    
    toursGrid.innerHTML = '';
    
    if (mockTours.length === 0) {
        toursGrid.innerHTML = '<p class="no-results">По вашему запросу туров не найдено. Попробуйте изменить параметры поиска.</p>';
        return;
    }
    
    mockTours.forEach(tour => {
        const tourCard = document.createElement('div');
        tourCard.className = 'tour-card';
        tourCard.setAttribute('data-region', tour.region);
        tourCard.setAttribute('data-type', tour.type);
        tourCard.setAttribute('data-price', getPriceRange(tour.price));
        
        tourCard.innerHTML = `
            <img src="${tour.image}" alt="${tour.title}">
            <div class="tour-card-content">
                <h3>${tour.title}</h3>
                <div class="meta">
                    <span><i class="fas fa-map-marker-alt"></i> ${getRegionName(tour.region)}</span>
                    <span class="type">${getTypeName(tour.type)}</span>
                </div>
                <p class="description">${tour.duration} тур по ${getTourDescription(tour.region)}</p>
                <div class="price">${tour.price.toLocaleString('ru-RU')} руб.</div>
                <a href="tour-detail.html?id=${tour.id}" class="btn">Подробнее</a>
            </div>
        `;
        toursGrid.appendChild(tourCard);
    });
}

// Вспомогательные функции для преобразования данных
function getRegionName(region) {
    const regions = {
        'central': 'Центральная Россия',
        'northwest': 'Северо-Запад',
        'south': 'Юг России',
        'kavkaz': 'Северный Кавказ',
        'siberia': 'Сибирь',
        'far-east': 'Дальний Восток'
    };
    return regions[region] || region;
}

function getTypeName(type) {
    const types = {
        'excursion': 'Экскурсионный',
        'active': 'Активный',
        'beach': 'Пляжный',
        'hourse': 'Конный'
    };
    return types[type] || type;
}

function getPriceRange(price) {
    if (price < 40000) return '0-20000';
    if (price < 50000) return '20000-50000';
    if (price < 70000) return '50000-100000';
    return '100000+';
}

